/**
 * Convert emoji unicode codepoints to Fluent Emoji 3D URL
 * Fluent emoji files are named by their unicode codepoints joined with underscores
 */
export function getFluentEmojiUrl(emoji: string): string {
	// Get unicode codepoints for the emoji
	const codepoints = [...emoji]
		.map((char) => char.codePointAt(0)?.toString(16).toLowerCase())
		.filter(Boolean)
		// Filter out variation selectors (fe0f) as Fluent doesn't use them in filenames
		.filter((cp) => cp !== 'fe0f');

	const filename = codepoints.join('_');

	// Use the 3D style from the Fluent UI emoji CDN
	// This CDN hosts the optimized 3D emoji assets
	return `https://raw.githubusercontent.com/AlttiRi/fluentui-emoji/main/3d/${filename}.png`;
}

/**
 * Get Fluent Emoji URL with fallback to Twemoji SVG
 */
export function getFluentEmojiUrlWithFallback(emoji: string, twemojiUrl: string): string {
	return getFluentEmojiUrl(emoji);
}

